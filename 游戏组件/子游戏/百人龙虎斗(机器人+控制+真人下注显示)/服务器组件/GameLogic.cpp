//混乱扑克
BYTE CGameLogic::RandCard()
{
	DWORD nTime = GetTickCount();
	for (int i = 0; i < 52; ++i)
	{
		BYTE Index = ((rand() + i + static_cast<BYTE>(nTime))%52);
		BYTE temp = m_cbCardListData[i];
		m_cbCardListData[i] = m_cbCardListData[Index];
		m_cbCardListData[Index] = temp;
	}
	nTime = GetTickCount();
	BYTE cbCrad = m_cbCardListData[(rand() + static_cast<BYTE>(nTime))%52];
	return cbCrad;
}

